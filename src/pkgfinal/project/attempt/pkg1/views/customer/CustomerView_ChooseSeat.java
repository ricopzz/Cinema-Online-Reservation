    private void checkSelected(JToggleButton butt){
        if (butt.isSelected()){
            butt.setBackground(Color.yellow);
            butt.setForeground(Color.yellow);
            butt.setOpaque(true);
        }
        else{
            butt.setBackground(Color.green);
            butt.setForeground(Color.green);
            butt.setOpaque(true);
        }
    }
